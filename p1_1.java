/* 
Difference between JVM, JRE, and JDK (Short Explanation):

JVM (Java Virtual Machine):

Runs Java bytecode (.class files).

Platform-dependent.

Example: Converts compiled code to machine code during execution.

JRE (Java Runtime Environment):

Contains JVM + libraries needed to run Java programs.

Can't compile code.

Example: Used to run existing Java apps.

JDK (Java Development Kit):

Contains JRE + tools like compiler (javac) for development.

Used to write, compile, and run Java code.

Example: Needed for coding in Java.

Two Popular Java IDEs:

IntelliJ IDEA

Eclipse
*/